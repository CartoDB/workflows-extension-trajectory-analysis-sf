CREATE OR REPLACE TEMP FUNCTION _TRAJECTORYCLEANER_DRYRUN_QUERY(
    INPUT_TABLE STRING,
    OUTPUT_TABLE STRING
)
RETURNS STRING NOT NULL
LANGUAGE JAVASCRIPT
AS
$$
    return `
        CREATE OR REPLACE TABLE ${OUTPUT_TABLE} AS (
            SELECT *, '' AS logs 
            FROM ${INPUT_TABLE} 
            WHERE FALSE
        )
    `;
$$;

EXECUTE IMMEDIATE _TRAJECTORYCLEANER_DRYRUN_QUERY(
    :input_table,
    :output_table
);
